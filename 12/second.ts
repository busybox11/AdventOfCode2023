let sum = 0;

function calculateCombinations(
  current: string,
  goal: number[],
  currentIndex: number,
  goalIndex: number,
  goalGroupIndex: number,
  cache: { [key: string]: number } = {}
) {
  const key = `${currentIndex},${goalIndex},${goalGroupIndex}`;

  if (key in cache) return cache[key];

  if (currentIndex === current.length) {
    return goalIndex >= goal.length - 1 &&
      goalGroupIndex === (goal[goalIndex] || 0)
      ? 1
      : 0;
  }

  let result = 0;

  const currentChar = current[currentIndex];
  const nextIndex = currentIndex + 1;

  if (currentChar === "#" || currentChar === "?") {
    result += calculateCombinations(
      current,
      goal,
      nextIndex,
      goalIndex,
      goalGroupIndex + 1,
      cache
    );
  }

  if (currentChar === "." || currentChar === "?") {
    if (
      goalGroupIndex === 0 ||
      (goalGroupIndex > 0 &&
        goalIndex < goal.length &&
        goal[goalIndex] === goalGroupIndex)
    ) {
      result += calculateCombinations(
        current,
        goal,
        nextIndex,
        goalIndex + (goalGroupIndex > 0 ? 1 : 0),
        0,
        cache
      );
    }
  }

  return (cache[key] = result);
}

function getPossibleCombinations(tile) {
  const current = Array(5).fill(tile.data).join("?");
  const goal = Array(5).fill(tile.expected).flat();

  const result = calculateCombinations(current, goal, 0, 0, 0);

  return result;
}

tiles.forEach((tile) => {
  sum += getPossibleCombinations(tile);
});

